package JavaSeries.NestingThings;

public class NestedForLoop {
    public static void main(String[] args) {
        for(int i = 9; i >= 0; i--) {
            for(int k = 9; k >= 0; k--) {
                System.out.print(k + " " );
            }
            System.out.println();

        }
    }
}
